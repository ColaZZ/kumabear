@auth.route('/confirm/<token>')
@login_required
def confirm(token):
    if current_user.confirmed:
        return redirect(url_for('main.index'))
    if current_user.confirm(token):
        flash(u'你已经确认了你的账户.谢谢!')
    else:
        flash(u'确认链接已经失效或者过期了.')
    return redirect(url_for('main.index'))


